def format_designer_prompt(answer: str) -> str:
    """組合出題 prompt（含題庫）"""
    bank_text = "\n".join(f"- {q}" for q in QUESTION_BANK)
    system = DESIGNER_SYSTEM_PROMPT.format(question_bank=bank_text)
    user = DESIGNER_USER_PROMPT.format(answer=answer)
    return system, user
